export default function FilterBar({ active, onChange }: Props) {
  function toggle(cat: Category) {
    if (active.includes(cat)) {
      onChange(active.filter(c => c !== cat));
    } else {
      onChange([...active, cat]);
    }
  }

  return (
    <div className="flex items-center justify-center gap-6 overflow-x-auto no-scrollbar">
      {ALL_CATEGORIES.map(cat => {
        const isActive = active.includes(cat);
        return (
          <button
            key={cat}
            onClick={() => toggle(cat)}
            className={`shrink-0 flex flex-col items-center gap-1 pb-2 pt-1 px-1 text-[11px] font-medium transition-all duration-150 cursor-pointer border-b-2 ${
              isActive
                ? 'border-zinc-900 text-zinc-900'
                : 'border-transparent text-zinc-400 hover:text-zinc-600 hover:border-zinc-200'
            }`}
          >
            <span className={isActive ? 'text-zinc-900' : 'text-zinc-400'}>
              {CATEGORY_ICONS[cat]}
            </span>
            {CATEGORY_LABELS[cat]}
          </button>
        );
      })}
    </div>
  );
}
